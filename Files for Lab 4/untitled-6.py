

# Importing Math library for value Of PI 
import math 
pi = math.pi 
sqrt = math.sqrt
# Function To Calculate Side Surface Area of Cone
def area_of_cone(height, radius):
   return  pi * radius * sqrt(radius**2 + height**2)
